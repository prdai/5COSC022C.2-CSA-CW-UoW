package com.w2120198.csa.cw.model;

public class Sensor {

    // the one status that has behaviour: SensorReadingService blocks readings when it matches
    public static final String STATUS_MAINTENANCE = "MAINTENANCE";

    private String id;
    private String type;
    // kept as String to match the spec POJO, an enum would be nicer for type safety but drifts from the template
    private String status;
    private double currentValue;
    private String roomId;

    public Sensor() {
    }

    public Sensor(String id, String type, String status, double currentValue, String roomId) {
        this.id = id;
        this.type = type;
        this.status = status;
        this.currentValue = currentValue;
        this.roomId = roomId;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public double getCurrentValue() {
        return currentValue;
    }

    public void setCurrentValue(double currentValue) {
        this.currentValue = currentValue;
    }

    public String getRoomId() {
        return roomId;
    }

    public void setRoomId(String roomId) {
        this.roomId = roomId;
    }
}
